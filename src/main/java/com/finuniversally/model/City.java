package com.finuniversally.model;

public class City {
	private long id;
	private String name;
	private String countryCode;
	private String kabol;
	private String populaction;
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCountryCode() {
		return countryCode;
	}
	public void setCountryCode(String countryCode) {
		this.countryCode = countryCode;
	}
	public String getKabol() {
		return kabol;
	}
	public void setKabol(String kabol) {
		this.kabol = kabol;
	}
	public String getPopulaction() {
		return populaction;
	}
	public void setPopulaction(String populaction) {
		this.populaction = populaction;
	}
}
